#include <iostream>
using namespace std;
int main(){
    int a = 10;
    int b = 5;
    // If statement
    if (a > b) {
        cout << "a is greater than b" << endl;
    }
    // If-else statement
    if (a < b) {    
        cout << "a is less than b" << endl;
    } else {
        cout << "a is not less than b" << endl;
    }
    return 0;
}