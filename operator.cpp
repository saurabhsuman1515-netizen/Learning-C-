#include <iostream>
using namespace std;
int main(){
    int a = 10;
    int b = 5;  
    // Addition
    int sum = a + b;
    // Subtraction
    int difference = a - b;
    // Multiplication
    int product = a * b;
    // Division
    int quotient = a / b;
    // Modulus
    int remainder = a % b;

    // Output the results
    cout << "Sum: " << sum << endl;
    cout << "Difference: " << difference << endl;
    cout << "Product: " << product << endl;
    cout << "Quotient: " << quotient << endl;
    cout << "Remainder: " << remainder << endl;

    return 0;
}